public class EncapsulatedCalendar {
  private int day;
  private int month;
  private int year ;

  // default constructor and(?) overload constructors

  public setMonth(int month){
    this.month = month;
  }
  public setDay(int day){
    this.day = day;
  }
  public setYear(int year){
    this.year = year;
  }

  public getMonth(){
    return month;
  }
  public getDay(){
    return day;
  }
  public getYear(){
    return year;
  }
}
